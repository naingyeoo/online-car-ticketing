package entity;

public class User {
	int uid;
	String name;
	String gender;
	String email;
	String phone;
	String busname;
	String deparaturedate;
	String leavingfrom;
	String goingto;
	String deparaturetime;
	String seatno;
	String price;
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getBusname() {
		return busname;
	}
	public void setBusname(String busname) {
		this.busname = busname;
	}
	public String getDeparaturedate() {
		return deparaturedate;
	}
	public void setDeparaturedate(String deparaturedate) {
		this.deparaturedate = deparaturedate;
	}
	public String getLeavingfrom() {
		return leavingfrom;
	}
	public void setLeavingfrom(String leavingfrom) {
		this.leavingfrom = leavingfrom;
	}
	public String getGoingto() {
		return goingto;
	}
	public void setGoingto(String goingto) {
		this.goingto = goingto;
	}
	public String getDeparaturetime() {
		return deparaturetime;
	}
	public void setDeparaturetime(String deparaturetime) {
		this.deparaturetime = deparaturetime;
	}
	public String getSeatno() {
		return seatno;
	}
	public void setSeatno(String seatno) {
		this.seatno = seatno;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	
}
